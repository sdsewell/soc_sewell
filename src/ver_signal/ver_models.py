    h_km = h / 1000
    fig, ax = plt.subplots(figsize=(7, 6))

    ax.plot(DayglowEmission(h), h_km, label="Dayglow")
 
    ax.plot(
        DayglowEmissionModel[:, 0] * 100**3 / 4 / np.pi / 10,  # emission, original order
        DayglowEmissionModel[:, 1],                              # altitude km, original order
        ".",
        label="_nolegend_",
    )

    ax.plot(NightglowEmission(h), h_km, label="Nightglow")
    ax.plot(
        NightglowEmissionData[:, 0] * 100**3 / 4 / np.pi,
        NightglowEmissionData[:, 1],
        ".",
        label="_nolegend_",
    )

    ax.set_xlabel(r"Volume Emission Rate [m$^{-3}$ s$^{-1}$ sr$^{-1}$]")
    ax.set_ylabel("Height [km]")
    ax.set_title("Emission Models")
    ax.legend()

    plt.tight_layout()
    plt.show()


